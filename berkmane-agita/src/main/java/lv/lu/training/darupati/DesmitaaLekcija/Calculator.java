package lv.lu.training.darupati.DesmitaaLekcija;

public class Calculator {
    public int sum (int a, int b){
        return a+b;
    }

    //metode
    public int subtract (int a, int b){
        return a-b;
    }

    public int divide (int a, int b){
        return a/b;
    }

    public boolean isOdd(int number){
        return number%2 !=0;
    }
}
