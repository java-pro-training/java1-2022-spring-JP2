package lv.lu.training.lesson6;

public class ForPrint10Numbers {
    public static void main(String[] args) {
        //Forward
        for (int i = 1; i <= 10; i++) {
            System.out.println("i = " +i);
        }
        //Backward
        for (int i = 10; i > 0; i--) {
            System.out.println("i = " +i);
        }

    }
}
